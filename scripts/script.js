/**
 * Returns the sum of n1 and n2
 * @param {number} n1
 * @param {number} n2
 * @returns {number} sum of n1 and n2
 */
function add(n1, n2) {
  return n1 + n2;
}

/**
 * Returns the result of n2 subtracted from n1
 * @param {number} n1 
 * @param {number} n2 
 * @returns result of n2 subtracted from n1
 */
function subtract(n1, n2) {
  return n1 - n2;
}
